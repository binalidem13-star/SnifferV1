def ban():
    for line in banner.splitlines():
        print(line)
        time.sleep(0.030)

def dns_callback(packet):
    try:
        if packet.haslayer(DNS) and packet.haslayer(DNSQR):
            query = packet[DNSQR].qname.decode('utf-8', errors='ignore')
            # Remove trailing dot if present
            if query.endswith('.'):
                query = query[:-1]
                
            src_ip = packet['IP'].src if packet.haslayer('IP') else "Unknown"
            print(f"[DNS] {src_ip:<15} requested -> {query}")
    except Exception:
        pass

def start_dns_sniffer():
    os.system("cls" if os.name == "nt" else "clear")
    ban()
    print("=======================================================")
    print("               LIVE DNS REQUEST SNIFFER")
    print("=======================================================")
    print("Intercepting website requests (DNS) from your network...")
    print("Press CTRL+C to stop and return to the main menu.")
    print("=======================================================\n")
    
    try:
        # Sniff only UDP port 53 (Standard DNS port)
        sniff(filter="udp port 53", prn=dns_callback, store=False)
    except KeyboardInterrupt:
        print("\n\n[!] DNS Sniffer stopped.")
    except Exception as e:
        print(f"\n[!] Error: {e}")
        
    print("\n=======================================================")
    input("Press ENTER to return to the main menu...")

if __name__ == "__main__":
    start_dns_sniffer()