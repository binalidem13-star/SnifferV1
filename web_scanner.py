def get_ip_info(url):
    try:
        domain = url.replace("https://", "").replace("http://", "").split("/")[0]
        ip = socket.gethostbyname(domain)
        print(f"    |- Target IP:         {ip}")
        print(f"    |- Target Domain:     {domain}")
        return domain, ip
    except Exception as e:
        print(f"    |- [!] Failed to resolve IP: {e}")
        return None, None

def get_http_headers(url):
    try:
        if not url.startswith("http"):
            url = "http://" + url
        response = requests.head(url, timeout=5, allow_redirects=True)
        print("\n[+] HTTP HEADERS & SERVER INFO")
        
        server = response.headers.get("Server", "Hidden/Unknown")
        powered_by = response.headers.get("X-Powered-By", "Hidden/Unknown")
        
        print(f"    |- Server:            {server}")
        print(f"    |- X-Powered-By:      {powered_by}")
        print(f"    |- Status Code:       {response.status_code}")
        
        # Check security headers
        security_headers = {
            "Strict-Transport-Security": "HSTS",
            "X-Frame-Options": "X-Frame-Options",
            "X-Content-Type-Options": "X-Content-Type-Options",
            "Content-Security-Policy": "CSP"
        }
        
        print("\n[+] SECURITY HEADERS")
        for header, name in security_headers.items():
            status = "Enabled" if header in response.headers else "MISSING"
            print(f"    |- {name:<20} : {status}")
            
    except requests.exceptions.RequestException as e:
        print(f"\n[!] HTTP Request failed: {e}")

def check_hidden_paths(url):
    print("\n[+] SCANNING FOR HIDDEN PATHS & ADMIN PANELS")
    if not url.startswith("http"):
        url = "http://" + url
        
    common_paths = [
        "robots.txt", "sitemap.xml", ".git/", ".env", 
        "admin/", "login/", "wp-admin/", "api/", "api/v1/"
    ]
    
    found = False
    for path in common_paths:
        target = f"{url.rstrip('/')}/{path}"
        try:
            response = requests.head(target, timeout=3)
            if response.status_code in [200, 401, 403]:
                print(f"    |- [FOUND] {path:<15} (Status: {response.status_code})")
                found = True
        except requests.exceptions.RequestException:
            pass
            
    if not found:
        print("    |- No common hidden paths found.")

