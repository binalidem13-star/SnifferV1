COMMON_PORTS = {
    21: "FTP",
    22: "SSH",
    23: "Telnet",
    25: "SMTP",
    53: "DNS",
    80: "HTTP",
    110: "POP3",
    135: "RPC",
    139: "NetBIOS",
    143: "IMAP",
    443: "HTTPS",
    445: "Microsoft-DS",
    993: "IMAPS",
    995: "POP3S",
    1723: "PPTP",
    3306: "MySQL",
    3389: "RDP",
    5900: "VNC",
    8080: "HTTP-Proxy"
}

def ban():
    for line in banner.splitlines():
        print(line)
        time.sleep(0.030)

def get_surrounding_wifis():
    try:
        cmd = subprocess.run(["netsh", "wlan", "show", "networks"], capture_output=True, text=True, errors='ignore')
        output = cmd.stdout
        
        wifis = re.findall(r"SSID \d+ : (.+)", output)
        cl_wifi = [name.strip() for name in wifis if name.strip()]
        return cl_wifi
    except Exception:
        return []

def get_router_ip():
    try:
        ip_cmd = subprocess.run(["ipconfig"], capture_output=True, text=True, errors='ignore')
        gateway = re.search(r"(?:Standardgateway|Default Gateway)[\.\s]*:\s*([0-9\.]+)", ip_cmd.stdout)
        if gateway:
            return gateway.group(1)
    except Exception:
        pass
    return None

def check_port(ip, port):
    sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    sock.settimeout(1)
    try:
        result = sock.connect_ex((ip, port))
        if result == 0:
            return port, "OPEN", COMMON_PORTS.get(port, "Unknown")
        return port, "CLOSED", COMMON_PORTS.get(port, "Unknown")
    except Exception:
        return port, "ERROR", "Unknown"
    finally:
        sock.close()

def scan_ports(ip):
    print(f"\n[*] Scanning common ports on Router ({ip})...")
    print("    This might take a moment.\n")
    print(f"    {'PORT':<8} | {'STATE':<8} | {'SERVICE'}")
    print("    " + "-" * 35)
    
    open_ports = 0
    with ThreadPoolExecutor(max_workers=10) as executor:
        futures = [executor.submit(check_port, ip, port) for port in COMMON_PORTS.keys()]
        
        for future in futures:
            port, state, service = future.result()
            if state == "OPEN":
                print(f"    {str(port):<8} | \033[92m{state:<8}\033[0m | {service}")
                open_ports += 1
            else:
                print(f"    {str(port):<8} | \033[91m{state:<8}\033[0m | {service}")

    print("\n[+] Scan Summary:")
    print(f"    |- Total Ports Scanned: {len(COMMON_PORTS)}")
    print(f"    |- Open Ports Found:    {open_ports}")
    
    if open_ports > 3:
        print("\n[!] WARNING: Many open ports detected. This could be a security risk.")
        print("    Consider closing unused ports (like Telnet/23 or FTP/21) in your router settings.")

def start_port_scanner():
    os.system("cls" if os.name == "nt" else "clear")
    ban()
    print("=======================================================")
    print("               WIFI ROUTER PORT SCANNER")
    print("=======================================================")
    
    wifi_list = get_surrounding_wifis()
    
    if not wifi_list:
        print("[!] No networks found.")
        input("\nPress ENTER to return to the main menu...")
        return

    print("Select a network to analyze its ports:")
    print("Note: You must be connected to the network to scan its router.")
    print("---")
    
    for index, wlan_name in enumerate(wifi_list, start=1):
        print(f"[{index}] {wlan_name}")
    
    print("-" * 40)
    print("[0] Back to the main menu")
    
    try:
        choice = int(input("\nSelect network number: "))
    except ValueError:
        print("[!] Invalid input.")
        time.sleep(2)
        return

    if choice == 0:
        return
        
    if 1 <= choice <= len(wifi_list):
        selected_wifi = wifi_list[choice - 1]
        
        os.system("cls" if os.name == "nt" else "clear")
        ban()
        print(f"\n[*] Selected Network: {selected_wifi}")
        
        router_ip = get_router_ip()
        
        if router_ip:
            scan_ports(router_ip)
        else:
            print("\n[-] Error: Could not determine Router IP.")
            print("    Make sure you are actively connected to this Wi-Fi network.")
            
        print("\n=======================================================")
        input("Press ENTER to return to the main menu...")
    else:
        print("[!] Invalid choice.")
        time.sleep(2)

if __name__ == "__main__":
    start_port_scanner()